package com.google.android.apps.common.testing.accessibility.framework.replacements;

/**
 * Indicates that the implementing class is capable of serving as a replacement (for purposes of
 * accessibility evaluation) of a type @link T from the Android SDK, whose implementation is only
 * available in Android runtime environments.
 */
public interface Replaceable {

    /**
     * Creates a native Android-compatible object of type {@link T} that is acceptable for use in
     * Android environments.
     *
     * <p>NOTE: This method is intended to function only in Android runtime environments. Invoking
     * this in another runtime will result in {@link RuntimeException}s generated by Android's SDK
     * stub implementations.
     *
     * @return an instance of the Android-compatible class that is being replaced by this instance
     */
}
